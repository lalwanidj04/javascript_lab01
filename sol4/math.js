function add(a,b){
    const sum = a + b;
    return sum;
}
function subtract(a,b){
    const diff = a - b;
    return diff;
}
function multiply(a,b){
    const product = a*b;
    return product;
}
function divide(a,b){
    const quotient = a/b;
    return quotient;
}
module.exports = {add,subtract,multiply,divide};